    function bazele(puncte)
        i=1;
        nr=1;
        while nr<=3
            plot(puncte(1,i:i+3), puncte(2,i:i+3),'b*-'); 
            hold on;
            x=puncte(:,i:i+3)*B;
            plot(x(1,:),x(2,:),'Color',[0,1,0]);
            plot(x(1, 1), x(2, 1),'r*')
            plot(x(1, end), x(2, end), 'r*')
            nr=nr+1;
            i=i+3;
        end     
    end

